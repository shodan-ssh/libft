#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	int i;

	i = 0;
	if (s1 && s2 && n)
	{
		while (n)
		{
			if (*s1++ != *s2++)
				return (0);
			n--;
		}
	}
	return (1);
}
