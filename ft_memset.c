#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t			i;
	char			*sec_s;

	i = 0;
	sec_s = s;
	while (i < n)
	{
		sec_s[i] = c;
		i++;
	}
	return (sec_s);
}
