#include "libft.h"

int		ft_atoi(const char *str)
{
	int i;
	int nb;
	int signe;

	i = 0;
	nb = 0;
	signe = 1;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
		if (str[i++] == '-')
			signe = -1;
	while (str[i] >= 48 && str[i] <= 57)
		nb = nb * 10 + (str[i++] - 48);
	return (nb * signe);
}
